// tours-within/:distance/center/:latlng/unit/:unit
// /tours-within/233/center/34.111745,-118.113491/unit/mi
exports.getToursWithin = catchAsync(async (req, res, next) => {
  const { distance, latlng, unit } = req.params;
  const [lat, lng] = latlng.split(',');
  // radius of the earth in miles
  const radius = unit === 'mi' ? distance / 3963.2 : distance / 6378.1;
  // console.log(distance, lat, lng, unit);
  if (!lat || !lng) {
    next(
      new AppError(
        'Please provide latitude and longitude in the format lat,lng.',
        400
      )
    );
  }
  // console.log(distance, lat, lng, unit);
  // console.log(radius);
  // console.log(lat, lng, radius);
  // This is a geospatial query to find all the tours within a certain distance from a certain point, we are using $geoWithin operator. $centerSphere is used to define the circle.

  const tours = await Tour.find({
    startLocation: { $geoWithin: { $centerSphere: [[lng, lat], radius] } }
  });
  // console.log(tours);
  res.status(200).json({
    status: 'success',
    results: tours.length,
    data: {
      data: tours
    }
  });
});
exports.getDistances = catchAsync(async (req, res, next) => {
  const { latlng, unit } = req.params;
  const [lat, lng] = latlng.split(',');
  // radius of the earth in miles
  // const radius = unit === 'mi' ? distance / 3963.2 : distance / 6378.1;
  // console.log(distance, lat, lng, unit);
  if (!lat || !lng) {
    next(
      new AppError(
        'Please provide latitude and longitude in the format lat,lng.',
        400
      )
    );
  }
  const mul = unit === 'mi' ? 0.000621371 : 0.001;
  const distance = await Tour.aggregate([
    {
      $geoNear: {
        // since we only have one geospatial index in our model we don't have to specify key or index
        near: {
          type: 'Point',
          coordinates: [lng * 1, lat * 1]
        },
        distanceField: 'distance',
        distanceMultiplier: mul
      }
    },
    {
      $project: {
        distance: 1,
        name: 1
      }
    }
  ]);

  res.status(200).json({
    status: 'success',

    data: {
      data: distance
    }
  });
});
